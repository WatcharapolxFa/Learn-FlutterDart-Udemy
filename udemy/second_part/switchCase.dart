void main(List<String> args) {
  var day  = 'Monday';
  switch (day){
     case 'Monday':
       print('Today is Monday');
       break;
      case 'Tuesday':
       print('Today is Tuesday');
       break;
      case 'Wednesday':
        print('Today is Wednesday');
        break;
      default:
        print('Today is not a day');
  }
}